    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        int codigo = Integer.parseInt(TextCodigo.getText());
        String nombre = String.valueOf(TextNombre.getText());
        String contraseña = String.valueOf(TextContraseña.getText());
        
        b.addRow(new Object[]{codigo,nombre,contraseña});
        TextCodigo.setText("");
        TextNombre.setText("");
        TextContraseña.setText("");
        
    }//GEN-LAST:event_BtnAgregarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        b.removeRow(jTable1.getSelectedRow());
    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        b.setValueAt(TextCodigo.getText(), jTable1.getSelectedRow(),0);
        b.setValueAt(TextNombre.getText(), jTable1.getSelectedRow(),1);
        b.setValueAt(TextContraseña.getText(), jTable1.getSelectedRow(),2);

        
        TextCodigo.setText("");
        TextNombre.setText("");
        TextContraseña.setText("");
    }//GEN-LAST:event_BtnActualizarActionPerformed


